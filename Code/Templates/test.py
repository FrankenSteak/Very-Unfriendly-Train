def analyze(data):
        lOut = []

        for i in range(0, len(data)):
                sum = 0.0

                for j in range(0, len(data[i])):
                        sum += data[i][j]

                mean = sum / len(data[i])
                
                deviation = 0.0

                for j in range(0, len(data[i])):
                        deviation += np.power( (data[i][j] - mean), 2)

                deviation = deviation / len(data[i])
                deviation = np.sqrt( deviation )

                median = np.median( np.array(data[i] ) )
                
                skewness = (3.0 * ( mean - median ) ) / deviation

                lOut.append([mean, deviation, median, skewness])

        return lOut

def standardize(data, stats):
        for i in range(0, len(data)):
                for j in range(0, len(data[i])):
                        data[i][j] = ( data[i][j] - stats[i][0] ) / stats[i][1]

        return data


                
